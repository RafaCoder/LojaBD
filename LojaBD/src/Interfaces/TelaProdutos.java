    private void preencherTabela(String SQL){
     ArrayList dados = new ArrayList();
     
     String [] Colunas = new String[]{"ID","Nome","Preço","Descrição"};
     
     conecta.executaSQL(SQL);
     
        try {
            conecta.rs.first();
            
            do{
                dados.add(new Object[]{conecta.rs.getInt("idProduto"),
                                       conecta.rs.getString("nomeProduto"),
                                       conecta.rs.getDouble("precoProduto"),
                                       conecta.rs.getString("descricaoProduto")});
            }while(conecta.rs.next());
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro ao preencher ArrayList! Erro:" + ex);
        }
     
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        Tabela.setModel(modelo);
        Tabela.getColumnModel().getColumn(0).setPreferredWidth(40);
        Tabela.getColumnModel().getColumn(0).setResizable(false);
        Tabela.getColumnModel().getColumn(1).setPreferredWidth(180);
        Tabela.getColumnModel().getColumn(1).setResizable(false);
        Tabela.getColumnModel().getColumn(2).setPreferredWidth(60);
        Tabela.getColumnModel().getColumn(2).setResizable(false);
        Tabela.getColumnModel().getColumn(3).setPreferredWidth(60);
        Tabela.getColumnModel().getColumn(3).setResizable(false);
        Tabela.getTableHeader().setReorderingAllowed(false);
        Tabela.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        Tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
        CaretListener listener = new CaretListener() {
        @Override
        public void caretUpdate(final CaretEvent e) {
            //faz as verificações necessárias.. tamanho de campo, validação, etc.
            if(campoID.getText().length() > 0){
                botaoExcluir.setEnabled(true);
                botaoAlterar.setEnabled(true);
                botaoAdicionar.setEnabled(false);
            }
            else{
                botaoExcluir.setEnabled(false);
                botaoAlterar.setEnabled(false);
                botaoAdicionar.setEnabled(true);
            }
        }
    };
    
