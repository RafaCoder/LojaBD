    private void PesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PesquisarMouseClicked
        if(campoDeBusca.getText().length()> 0){
            for(int i = 0; i < Tabela.getRowCount();i++){
                if(campoDeBusca.getText().equals(Tabela.getValueAt(i, 1))){
                    campoNome.setText(Tabela.getValueAt(i, 1).toString());
                    campoPreco.setText(Tabela.getValueAt(i, 2).toString());
                    campoID.setText(Tabela.getValueAt(i, 0).toString());                    
                    campoDescricao.setText(Tabela.getValueAt(i, 3).toString());
                }
            }
        }
    }//GEN-LAST:event_PesquisarMouseClicked

