    private void botaoAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAdicionarActionPerformed
        try {
            PreparedStatement pst = ConectaBanco.conn.prepareStatement("insert into Produto (nomeProduto, precoProduto, descricaoProduto) values(?, ?, ?);");
            pst.setString(1, campoNome.getText());
            pst.setString(2, campoPreco.getText());
            pst.setString(3, campoDescricao.getText());
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(rootPane, "Adicionado com Sucesso!");
            preencherTabela("select * from Produto order by idProduto;");
            limparCampos();            
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Erro ao Adicionar!\n ERRO: " + ex);
        }
    }//GEN-LAST:event_botaoAdicionarActionPerformed

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        if(campoID.getText().length() > 0){
            try {
                PreparedStatement pst = ConectaBanco.conn.prepareStatement("delete from  Produto where idProduto = ?;");
                pst.setString(1, campoID.getText());
                pst.execute();

                JOptionPane.showMessageDialog(rootPane, "Excluído com Sucesso!");
                preencherTabela("select * from Produto order by idProduto;");
                limparCampos();
            }
            catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane,"Erro ao Excluir!\n ERRO: " + ex);
            }
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed

