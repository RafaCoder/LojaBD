    private void botaoAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoAlterarMouseClicked
        if(campoID.getText().length() > 0){
            try {
                PreparedStatement pst = ConectaBanco.conn.prepareStatement("update Produto set nomeProduto = ?, precoProduto = ?, descricaoProduto = ? where idProduto = ?;");
                pst.setString(1, campoNome.getText());
                pst.setString(2, campoPreco.getText());
                pst.setString(3, campoDescricao.getText());
                pst.setString(4, campoID.getText());
                pst.execute();

                JOptionPane.showMessageDialog(rootPane, "Alterado com Sucesso!");
                preencherTabela("select * from Produto order by idProduto;");
                limparCampos();
            }
            catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane,"Erro ao Alterar!\n ERRO: " + ex);
            }
        }
    }//GEN-LAST:event_botaoAlterarMouseClicked

