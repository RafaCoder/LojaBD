    /**
     * @param args the command line arguments
     */
    private void populaClientes(){
        String sql = "SELECT nomePessoa FROM Pessoa ";
        
        try{
            PreparedStatement pst = ConectaBanco.conn.prepareStatement(sql);
            ConectaBanco.rs = pst.executeQuery();
            
            while(ConectaBanco.rs.next()){
                CampoCliente.addItem(ConectaBanco.rs.getString("nomePessoa"));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(TelaVendas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
